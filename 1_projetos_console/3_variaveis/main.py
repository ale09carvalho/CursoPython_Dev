# Declaraçao de variaveis
'''
Variáveis são espaços na memória do computador onde podemos armazenar dados.
Tipos de variáveis:
String: é como são chamados textos pequenos.
Int: são os números inteiros
Pontos flutuantes: são os números decimais. Float: são os números decimais.
booleanos: são valores binários de VERDADEIRO (True) ou FALSO (False),

'''
# Exemplo declarar variáveis
nome = "João" #String
idade = 30 # inteiro
altura = 1.75 # float
valor_booleano = True # booleano
programador = False #!SECTION

# Saida de dados
print("Nome:", nome)
print("Idade:", idade)
print("Altura:", altura)
print("Valor booleano:", valor_booleano)
print("Programador:", programador)


